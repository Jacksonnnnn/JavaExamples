package com.Jacksonnn.java.util;

import java.util.*;

public class HashSetDemo {

	public static void main(String[] args) {
		//Create a has set.
		HashSet<String> hs = new HashSet<String>();
		
		//Add elements to the Hash Set.
		hs.add("Beta");
		hs.add("Alpha");
		hs.add("Eta");
		hs.add("Gamma");
		hs.add("Epsilon");
		hs.add("Omega");
		
		System.out.println(hs);
	}

}
